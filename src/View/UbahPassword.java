    private void ChangePassBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ChangePassBTNActionPerformed
        String oldpass = OldPass.getText();
        String newpass = NewPass.getText();
        String confirmPass = KonfirmasiPass.getText();

        // Validasi password lama, baru, dan konfirmasi
        if (oldpass.isEmpty() || newpass.isEmpty() || confirmPass.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Semua field harus diisi", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Validasi password baru dan konfirmasi password
        if (!newpass.equals(confirmPass)) {
            JOptionPane.showMessageDialog(null, "Password baru tidak cocok!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Ambil user ID dari sesi
        int idUser = UserSession.getId();

        // Panggil metode untuk mengubah password
        boolean status = akunCNTRL.UbahPassword(idUser, oldpass, newpass);
        if (status) {
            JOptionPane.showMessageDialog(null, "Password berhasil diubah!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            // Bersihkan field setelah password berhasil diubah
            OldPass.setText("");
            NewPass.setText("");
            KonfirmasiPass.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Password lama salah!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_ChangePassBTNActionPerformed

